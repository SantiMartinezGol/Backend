class Product
{
  constructor(props)
  {
      this.id = props.id;
      this.title = props.title;
      this.description = props.description;
      this.code = props.code;
      this.price = props.price;
      this.stock = props.stock;
      this.category = props.category;
      this.status = props.status;
  }
}

export default Product;
